/**
 * Get random harvest success message
 * 
 * @param terrain - Terrain type harvested
 * @param amount - Amount harvested (for resource tiles)
 * @param itemType - Type of item found (for cave tiles)
 * @returns Formatted success message
 */
export function getHarvestSuccessMessage(
  terrain: TerrainType,
  amount?: number,
  itemType?: 'digger' | 'tradeable' | 'none'
): string {
  if (terrain === TerrainType.Metal) {
    const message = METAL_MESSAGES[Math.floor(Math.random() * METAL_MESSAGES.length)];
    return amount ? `${message}\n${amount.toLocaleString()} Metal` : message;
  }
  
  if (terrain === TerrainType.Energy) {
    const message = ENERGY_MESSAGES[Math.floor(Math.random() * ENERGY_MESSAGES.length)];
    return amount ? `${message}\n${amount.toLocaleString()} Energy` : message;
  }
  
  if (terrain === TerrainType.Cave) {
    if (itemType === 'none') {
      return CAVE_EMPTY_MESSAGES[Math.floor(Math.random() * CAVE_EMPTY_MESSAGES.length)];
    } else if (itemType === 'digger') {
      return CAVE_DIGGER_MESSAGES[Math.floor(Math.random() * CAVE_DIGGER_MESSAGES.length)];
    } else if (itemType === 'tradeable') {
      return CAVE_TRADEABLE_MESSAGES[Math.floor(Math.random() * CAVE_TRADEABLE_MESSAGES.length)];
    }
  }
  
  return 'Harvest successful!';
}

